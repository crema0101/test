# 탈출문자
# \n : 줄바꿈
print("백문이 불여일견\n백문이 불여일타")


# \" \' : 문장내에서 따옴표 표시
print("저는 '나도코딩' 입니다")
print("저는 \"나도코딩\" 입니다")
print("저는 \'나도코딩\' 입니다")

#\\ : 문장내에서 사용 (경로를 추출할 때는 \하나만 써서는 출력되지 않음(오류남)
print("C:\\Users\\최경삼\\PythonWorkspace>")

# \r : 커서를 맨 앞으로 이동
print("Red Apple\rPine")

# \b : 백스페이스 (한 글자 삭제)
print("백스페이스 한글자 삭제")
print("Redd\bApple")

# \t : 탭
print("Red\tApple")

